def system_prompt(client_name: str | None = None) -> str:
    """Сборка system prompt для звонаря.

    Контент читается из БД (`shared.agent_config`) с TTL-кэшем 60с, чтобы
    клиент через `/admin/scripts` мог менять блоки и акции без рестарта.
    Если БД недоступна или блоки пустые — используется fallback на
    константы ниже (PRODUCT_FACTS / SCRIPT_VERBATIM / etc.). Это гарантия
    что агент не падает при любых правках.
    """
    from shared import agent_config  # lazy: избегаем циклов на старте

    agent_name = agent_config.get_setting("agent_name", AGENT_NAME)
    school_name = agent_config.get_setting("school_name", SCHOOL_NAME)
    city = agent_config.get_setting("city", "Алматы")

    name_hint = (
        f"\nИмя клиента из заявки: {client_name}. Используй его в обращении."
        if client_name else ""
    )

    blocks = agent_config.get_blocks(scope="voice")
    blocks_text = "\n\n".join(b.body.strip() for b in blocks if b.body.strip())

    promos = agent_config.get_active_promotions(scope="voice")
    if promos:
        promo_lines = ["Сейчас действуют акции — обязательно упомяни их при подходящем моменте:"]
        for p in promos:
            promo_lines.append(f"  • {p.title}: {p.body.strip()}")
        promos_text = "\n".join(promo_lines)
    else:
        promos_text = ""

    sections = [
        f"""Ты — AI-агент исходящих звонков футбольной школы {school_name}
в {city}. Тебя зовут {agent_name}. Ты звонишь по тёплой заявке (родитель
оставил контакты на пробную тренировку), цель — пройти квалификацию по
скрипту и записать ребёнка на пробную тренировку.

Тон — дружелюбный, профессиональный, без давления. Говори по-русски,
короткими фразами, не более 1–2 предложений за реплику. Если клиент
торопится — предложи перезвонить позже и попрощайся.{name_hint}

Главное правило: следуй скрипту. Не выдумывай вопросов вне скрипта,
не обещай скидок, не называй цен, кроме указанных в блоке про продукты.""",
        blocks_text,
        promos_text,
        """Дополнительные правила:
- Сначала подтверди личность клиента и факт заявки. Если клиент говорит,
  что заявку не оставлял — извинись, уточни «возможно, кто-то другой из
  семьи?», и при «нет» — корректно завершай.
- Пять квалификационных вопросов — обязательны и идут СТРОГО по порядку.
  Один вопрос за реплику. Не задавай два сразу.
- Если ребёнку младше 6 — лагерь не предлагай, тренировки уточни
  (наборные группы доступны с 4 лет).
- При возражении — отвечай шаблоном из скрипта, потом возвращайся к
  закрытию (запись на бесплатную пробную).
- При завершении ВСЕГДА подтверди, что отправишь информацию в WhatsApp
  на этот же номер.""",
    ]
    return "\n\n".join(s for s in sections if s.strip())


def stage_instruction(stage: Stage, *, child_age: int | None = None) -> str:
    if stage == "greet":
        return (
            "Поздоровайся. Если в system prompt дано имя клиента — обратись "
            f"к нему по имени; если имени нет — просто «Здравствуйте», без "
            f"плейсхолдеров. Назови себя: «Меня зовут {AGENT_NAME}, звоню с "
            f"детской футбольной школы {SCHOOL_NAME}». Одно предложение, без "
            "квадратных/угловых скобок и слов вроде «имя клиента»."
        )
    if stage == "confirm_request":
        return (
            "Спроси: «Вы оставляли заявку на футбольную секцию для Вашего "
            "ребенка?» — и если «да», скажи комплимент про вовлечённого родителя "
            "и попроси разрешения задать несколько вопросов."
        )
    if stage == "qualify":
        return (
            "Задай следующий неотвеченный квалификационный вопрос (по порядку):\n"
            "  1. Ребенок ранее тренировался?\n"
            "  2. Возраст ребенка и район проживания?\n"
            "  3. В какое время удобно (утром, вечером или после обеда)?\n"
            "  4. Когда хотите на пробную тренировку?\n"
            "  5. Почему решили отдать на футбол?\n"
            "Один вопрос за реплику. Не задавай несколько сразу."
        )
    if stage == "present":
        if child_age is not None and child_age < 6:
            return (
                "Возраст до 6 — лагерь НЕ предлагай. Назови ближайший филиал, "
                "удобное время и пригласи на бесплатную пробную тренировку."
            )
        return (
            "На основе ответов клиента назови подходящий филиал, время и адрес. "
            "Только факты из PRODUCT_FACTS, без выдумок. Заверши вопросом: "
            "«Когда удобно прийти?» или «WhatsApp на этом номере?»"
        )
    if stage == "objection":
        return (
            "Определи возражение и ответь шаблоном из скрипта:\n"
            "  «дорого» → боль клиента: здоровье, друзья, активность; "
            "напомни про бесплатную пробную.\n"
            "  «далеко» → группы выходного дня.\n"
            "  «подумаем» → запись на 1 бесплатный пробный, потом ответ.\n"
            "После ответа на возражение возвращайся к закрытию."
        )
    if stage == "close":
        return (
            "Подтверди запись (день, время, филиал), скажи что тренер "
            "свяжется для подтверждения, и подтверди отправку адреса в "
            "WhatsApp на этот же номер."
        )
    if stage == "goodbye":
        return "Поблагодари за разговор, попрощайся одной короткой фразой."
    return ""
