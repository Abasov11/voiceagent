"""LLM-роутинг для оценки диалогов и AI-звонаря — OpenAI-only.

Маршрутизация по длительности транскрипта (оптимизация расходов):
  - short tier (< 60s) → дешёвая модель (gpt-4.1-mini)
  - long tier (≥ 60s) → более мощная модель (gpt-4.1)

Anthropic выпилен 2026-05-11 по решению команды.
"""
from __future__ import annotations

import logging
from typing import Any, Literal

log = logging.getLogger(__name__)

Tier = Literal["short", "long"]


def pick_tier(duration_s: int | None) -> Tier:
    if duration_s is None or duration_s < 60:
        return "short"
    return "long"


class LlmClient:
    def __init__(
        self,
        *,
        openai_api_key: str | None = None,
        short_model: str,
        long_model: str,
        # Игнорируется. Anthropic выпилен; параметр оставлен только для обратной
        # совместимости со старыми вызовами (pipeline.py до 2026-05-11).
        anthropic_api_key: str | None = None,
        api_key: str | None = None,
    ) -> None:
        self._openai = None
        if openai_api_key:
            from openai import AsyncOpenAI
            self._openai = AsyncOpenAI(api_key=openai_api_key)
        self.short_model = short_model
        self.long_model = long_model

    def _model(self, tier: Tier) -> str:
        return self.short_model if tier == "short" else self.long_model

    async def score_dialog(
        self,
        *,
        transcript: str,
        criteria: list[str],
        tier: Tier = "short",
        max_tokens: int = 1024,
    ) -> dict[str, Any]:
        """Legacy scoring (числовые баллы по критериям) — для backfill.

        Новый pipeline использует `analyze_dialog`.
        """
        if self._openai is None:
            raise RuntimeError("OPENAI_API_KEY is not set")
        system = (
            "Ты — оценщик звонков отдела продаж детской футбольной школы 'VoiceAgent'. "
            "Оцени диалог по критериям, верни строго JSON: "
            '{"items":[{"criterion":...,"score":0-10,"comment":"..."}], "total_score":0-10, "funnel_stage":"dialog|deal|lost"}.'
        )
        user = (
            "Критерии оценки:\n- " + "\n- ".join(criteria) + "\n\nТранскрипт:\n" + transcript
        )
        resp = await self._openai.chat.completions.create(
            model=self._model(tier),
            max_tokens=max_tokens,
            messages=[
                {"role": "system", "content": system},
                {"role": "user", "content": user},
            ],
            response_format={"type": "json_object"},
        )
        choice = resp.choices[0]
        text = choice.message.content or ""
        usage = resp.usage.model_dump() if resp.usage else {}
        return {"provider": "openai", "model": resp.model, "raw_text": text, "usage": usage}

    async def analyze_dialog(
        self,
        *,
        transcript: str,
        duration_s: int | None,
        tier: Tier = "short",
        max_tokens: int = 4096,
    ) -> dict[str, Any]:
        """Двухтрековый коучинговый анализ диалога: отчёт менеджеру + отчёт РОПу.

        Использует фреймворки ФВР (Факт-Влияние-Развитие), STAR и GROW. В тексте
        отчётов запрещены числовые оценки; total_score и funnel_stage идут как
        служебные агрегаты для дашборда.

        JSON-схема ответа:
          {"for_manager": str, "for_rop": str,
           "total_score": float 0-10, "funnel_stage": "dialog|deal|lost"}
        """
        if self._openai is None:
            raise RuntimeError("OPENAI_API_KEY is not set")
        user = (
            f"Транскрипт:\n{transcript}\n\n"
            f"Длительность: {duration_s or 0} sec"
        )
        resp = await self._openai.chat.completions.create(
            model=self._model(tier),
            max_tokens=max_tokens,
            messages=[
                {"role": "system", "content": ANALYZE_DIALOG_SYSTEM_PROMPT},
                {"role": "user", "content": user},
            ],
            response_format={"type": "json_object"},
        )
        choice = resp.choices[0]
        text = choice.message.content or ""
        usage = resp.usage.model_dump() if resp.usage else {}
        return {"provider": "openai", "model": resp.model, "raw_text": text, "usage": usage}


